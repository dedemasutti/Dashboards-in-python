import pandas as pd
import matplotlib.pyplot as plt
import plotly.graph_objects as go

# Carregar os dados de vendas
dados_vendas = pd.read_csv('dados_vendas.csv')

# An치lise explorat칩ria dos dados
total_vendas = dados_vendas['Valor'].sum()
vendas_por_produto = dados_vendas.groupby('Produto')['Valor'].sum().reset_index()

# Gr치fico de barras com as vendas por produto usando Matplotlib
plt.figure(figsize=(25, 10))
plt.bar(vendas_por_produto['Produto'], vendas_por_produto['Valor'])
plt.xlabel('Produto')
plt.ylabel('Total de Vendas')
plt.title('Vendas por Produto')
plt.xticks(rotation=45)
plt.show()

# Gr치fico de pizza com as vendas por produto usando Plotly
fig = go.Figure(data=[go.Pie(labels=vendas_por_produto['Produto'],
                             values=vendas_por_produto['Valor'])])
fig.update_layout(title='Vendas por Produto')
fig.show()

# Exibir o total de vendas
print('Total de Vendas: $', total_vendas)

